package com.example.phonenumberauth;

public class SaveProfileClass {

    String id,phoneNumber,name,dob,gender,password;

    public SaveProfileClass(String id, String phoneNumber, String name, String dob, String gender, String password) {
        this.id = id;
        this.phoneNumber = phoneNumber;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
        this.password = password;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
